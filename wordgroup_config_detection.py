    subparsers.add_parser("summary", parents=[common], help="只汇总现有报告")

    run_parser = subparsers.add_parser("run", parents=[common], help="运行三项 Unity 检测并汇总")
    run_parser.add_argument("--send", action="store_true", help="检测完成后发送飞书")

