def explain_unity_failure(project_path: pathlib.Path, exit_code: int) -> str:
    if exit_code == 0:
        return ""
    resolved_project = resolve_project_path(project_path)
    log_path = repo_root_from_project(resolved_project) / "Result" / "wordgroup_config_detection_unity.log"
    if not log_path.exists():
        return f"Unity 检测执行失败，退出码 {exit_code}。"
    try:
        text = log_path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return f"Unity 检测执行失败，退出码 {exit_code}。"

    if "No valid Unity Editor license found" in text or "Please activate your license." in text:
        return "Unity 未激活，无法以批处理模式运行当前项目。请先在这台 Mac 上激活 Unity Editor License。"
    if "another unity instance is running with this project open" in text.lower():
        return "当前 Unity 工程已经在另一个 Unity 编辑器实例中打开。请先关闭该工程对应的 Unity，再重新执行检测。"
    if "An error occurred while resolving packages:" in text:
        failed_packages: list[str] = []
        for line in text.splitlines():
            stripped = line.strip()
            if ": Error when executing git command." in stripped:
                failed_packages.append(stripped.split(":", 1)[0])
        package_text = "、".join(dict.fromkeys(failed_packages)) if failed_packages else "GitHub 依赖包"
        return (
            f"Unity Package Manager 解析依赖失败，当前检测还没真正跑起来。失败包：{package_text}。\n"
            "请先确认这台机器能访问对应 GitHub 包，或将这些 Git URL 依赖改成本地/内网可访问的包源后再重试。"
        )
    if "Scripts have compiler errors." in text:
        return "Unity 工程存在编译错误，当前检测没有跑起来。请先修复脚本编译错误。"
    if "Aborting batchmode due to failure" in text:
        return "Unity BatchMode 执行失败，请查看实时日志或 Unity 日志。"

    tail = [line.strip() for line in text.splitlines()[-20:] if line.strip()]
    if tail:
        return f"Unity 检测执行失败，退出码 {exit_code}。最后日志：{tail[-1]}"
    return f"Unity 检测执行失败，退出码 {exit_code}。"


def sync_project_to_trigger_commit(
    settings: dict[str, Any],
    trigger_info: dict[str, Any] | None,
    state: JobState | None = None,
) -> None:
    trigger = trigger_info or {}
    commit = str(trigger.get("commit") or "").strip()
    short_commit = str(trigger.get("short_commit") or commit[:8]).strip()
    if not commit:
        return
    project_path_raw = str(settings.get("project_path") or "").strip()
    if not project_path_raw:
        raise ValueError("未配置 Unity 工程路径，无法在运行前同步本地项目。")

    project_path = resolve_project_path(pathlib.Path(project_path_raw))
    target_repo = repo_root_from_project(project_path)
    if not target_repo.is_dir() or not (target_repo / ".git").exists():
        raise FileNotFoundError(f"目标本地项目不是 Git 仓库：{target_repo}")

    state = state or STATE
    source_repo_raw = str(settings.get("git_repo_path") or "").strip()
    source_repo = pathlib.Path(source_repo_raw).expanduser().resolve() if source_repo_raw else target_repo
    remote = str(settings.get("git_remote") or "").strip()

    state.add_log_event("Git", f"开始同步本地项目到命中提交：{short_commit}")
    state.add_log_event("Git", f"目标仓库：{target_repo}")

    if source_repo != target_repo:
        state.add_log_event("Git", f"执行 fetch：{source_repo} {short_commit}")
        git_sync_command(target_repo, ["fetch", str(source_repo), commit], timeout=300)
    elif remote:
        state.add_log_event("Git", f"执行 fetch：{remote}")
        git_sync_command(target_repo, ["fetch", remote, "--prune"], timeout=300)

    git_sync_command(target_repo, ["reset", "--hard", commit], timeout=300)
    synced_head = git_sync_command(target_repo, ["rev-parse", "--short", "HEAD"], timeout=30)
    state.add_log_event("Git", f"本地项目已同步到：{synced_head}")


def build_diagnostics(
    project_path: pathlib.Path,
    unity_path: str | None = None,
    settings: dict[str, Any] | None = None,
) -> dict[str, Any]:
    resolved_project = resolve_project_path(project_path)
    diagnostics: dict[str, Any] = {
        "input_project_path": str(project_path.expanduser()),
        "resolved_project_path": str(resolved_project),
        "project_exists": resolved_project.is_dir(),
        "has_assets": (resolved_project / "Assets").is_dir(),
        "has_project_settings": (resolved_project / "ProjectSettings").is_dir(),
        "unity_version": read_project_unity_version(resolved_project),
        "unity_path": "",
        "unity_found": False,
        "result_dir": "",
        "result_dir_writable": False,
        "reports": {},
    }

    if is_unity_project(resolved_project):
        repo_root = repo_root_from_project(resolved_project)
        result_dir = repo_root / "Result"
        diagnostics["result_dir"] = str(result_dir)
        try:
            result_dir.mkdir(parents=True, exist_ok=True)
            probe = result_dir / ".wordgroup_config_detection_write_test"
            probe.write_text("ok", encoding="utf-8")
            probe.unlink()
            diagnostics["result_dir_writable"] = True
        except OSError as exc:
            diagnostics["result_dir_writable"] = False
            diagnostics["result_dir_error"] = str(exc)
        diagnostics["reports"] = {
            check["name"]: str((result_dir / check["report"]).exists())
            for check in build_checks(settings)
        }

    try:
        detected_unity = find_unity(unity_path, resolved_project if is_unity_project(resolved_project) else None)
        diagnostics["unity_path"] = detected_unity
        diagnostics["unity_found"] = True
        expected_version = diagnostics.get("unity_version") or ""
        if expected_version and f"/{expected_version}/" not in detected_unity:
            diagnostics["unity_warning"] = f"未找到精确版本，已回退到兼容版本：{detected_unity}"
    except Exception as exc:
        diagnostics["unity_error"] = str(exc)

    return diagnostics


