class JobState:
    def __init__(self) -> None:
        self.lock = threading.Lock()
        self.running = False
        self.started_at = ""
        self.finished_at = ""
        self.exit_code: int | None = None
        self.logs: list[str] = []
        self.error = ""
        self.summary: dict[str, Any] = {}
        self.project_id = ""
        self.project_name = ""
        self.runner_kind = "wordgroup_builtin"

    def reset(self, project_id: str = "", project_name: str = "", runner_kind: str = "wordgroup_builtin") -> None:
        with self.lock:
            self.running = True
            self.started_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            self.finished_at = ""
            self.exit_code = None
            self.logs = []
            self.error = ""
            self.summary = {}
            self.project_id = project_id
            self.project_name = project_name
            self.runner_kind = runner_kind

    def _format_log_line(self, category: str, detail: str, when: str | None = None) -> str:
        stamp = when or datetime.now().strftime("%H:%M:%S")
        return f"[{category}][{stamp}]{detail}"

    def add_log_event(self, category: str, detail: str, when: str | None = None) -> None:
        detail = str(detail or "").strip()
        if not detail:
            return
        self.add_log(self._format_log_line(category, detail, when=when))

    def add_multiline_log(self, category: str, text: str, when: str | None = None) -> None:
        for line in str(text or "").splitlines():
            self.add_log_event(category, line, when=when)

    def _normalize_log_line(self, line: str) -> str:
        if re.match(r"^\[[^\[\]]+\]\[\d{2}:\d{2}:\d{2}\]", line):
            return line
        if line.startswith("[UnityLog] "):
            return self._format_log_line("Unity", line[len("[UnityLog] "):].strip())
        if line.startswith("[EditorBridge] "):
            return self._format_log_line("桥接", line[len("[EditorBridge] "):].strip())
        if line.startswith("Traceback "):
            return self._format_log_line("错误", line)
        if line.startswith("  File "):
            return self._format_log_line("错误", line)
        lowered = line.lower()
        if lowered.startswith("feishu:"):
            return self._format_log_line("飞书", line.split(":", 1)[1].strip())
        if lowered.startswith("unity:"):
            return self._format_log_line("Unity", line.split(":", 1)[1].strip())
        if lowered.startswith("project:"):
            return self._format_log_line("运行", "工程：" + line.split(":", 1)[1].strip())
        if lowered.startswith("log:"):
            return self._format_log_line("运行", "日志：" + line.split(":", 1)[1].strip())
        if lowered.startswith("command:"):
            return self._format_log_line("运行", "命令：" + line.split(":", 1)[1].strip())
        if lowered.startswith("working directory:"):
            return self._format_log_line("运行", "工作目录：" + line.split(":", 1)[1].strip())
        if lowered.startswith("unity process timeout:"):
            return self._format_log_line("Unity", "进程总超时设置：" + line.split(":", 1)[1].strip())
        if line.startswith("项目："):
            return self._format_log_line("运行", line)
        if "git 命中提交" in line.lower():
            return self._format_log_line("Git", line)
        if line == "命中文件：":
            return self._format_log_line("Git", line)
        if line.startswith("  - "):
            return self._format_log_line("Git", line[4:].strip())
        if "自检" in line:
            return self._format_log_line("自检", line)
        return self._format_log_line("系统", line)

    def add_log(self, line: str) -> None:
        for raw_line in str(line or "").splitlines():
            clean_line = raw_line.rstrip()
            if not clean_line:
                continue
            normalized = self._normalize_log_line(clean_line)
            with self.lock:
                self.logs.append(normalized)
                self.logs = self.logs[-500:]

    def finish(self, exit_code: int, summary: dict[str, Any] | None = None, error: str = "") -> None:
        with self.lock:
            self.running = False
            self.finished_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            self.exit_code = exit_code
            self.error = error
            if summary is not None:
                self.summary = summary

    def snapshot(self) -> dict[str, Any]:
        with self.lock:
            return {
                "running": self.running,
                "started_at": self.started_at,
                "finished_at": self.finished_at,
                "exit_code": self.exit_code,
                "logs": list(self.logs),
                "error": self.error,
                "summary": self.summary,
                "project_id": self.project_id,
                "project_name": self.project_name,
                "runner_kind": self.runner_kind,
            }


