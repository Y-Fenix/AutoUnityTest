class MultiProjectApp:
    def __init__(self, args: argparse.Namespace) -> None:
        self.args = args
        self.lock = threading.Lock()
        self.store_path = PROJECTS_STORE_PATH
        self.states_dir = PROJECT_STATES_DIR
        self.states_dir.mkdir(parents=True, exist_ok=True)
        self.projects: list[dict[str, Any]] = []
        self.selected_project_id = ""
        self.watchers: dict[str, GitWatchController] = {}
        self._load()
        if self._dedupe_projects():
            self._save()
        if not self.projects:
            self._create_default_project()
        self._ensure_selected_project()
        self._rebuild_watchers()

    def _base_defaults(self) -> dict[str, Any]:
        defaults = build_ui_defaults(self.args)
        defaults["runner_kind"] = "wordgroup_builtin"
        defaults["run_command"] = ""
        defaults["send_command"] = ""
        defaults["name"] = derive_project_name(defaults)
        return defaults

    def _normalize_profile(self, raw: dict[str, Any] | None) -> dict[str, Any]:
        profile = self._base_defaults()
        profile.update(raw or {})
        name = derive_project_name(profile)
        profile["name"] = name
        profile["project_name"] = name
        profile["id"] = str(profile.get("id") or "").strip()
        runner_kind = str(profile.get("runner_kind") or "wordgroup_builtin").strip()
        if runner_kind not in {"wordgroup_builtin", "shell_command"}:
            runner_kind = "wordgroup_builtin"
        profile["runner_kind"] = runner_kind
        builtin_methods = default_builtin_methods(profile)
        raw_profile = raw or {}
        for field in method_field_names():
            value = str(profile.get(field) or "").strip()
            raw_value = str(raw_profile.get(field) or "").strip()
            should_upgrade_legacy = (
                runner_kind == "wordgroup_builtin"
                and builtin_methods[field] != LEGACY_BUILTIN_METHODS[field]
                and value == LEGACY_BUILTIN_METHODS[field]
                and raw_value in {"", LEGACY_BUILTIN_METHODS[field]}
            )
            if should_upgrade_legacy or not value:
                value = builtin_methods[field]
            profile[field] = value
        for field in ("report_config", "report_language", "report_image"):
            value = str(profile.get(field) or "").strip()
            raw_value = str(raw_profile.get(field) or "").strip()
            should_upgrade_legacy = (
                runner_kind == "wordgroup_builtin"
                and builtin_methods[field] != LEGACY_BUILTIN_REPORTS[field]
                and value == LEGACY_BUILTIN_REPORTS[field]
                and raw_value in {"", LEGACY_BUILTIN_REPORTS[field]}
            )
            if should_upgrade_legacy or not value:
                value = builtin_methods[field]
            profile[field] = value
        profile["run_command"] = str(profile.get("run_command") or "").strip()
        profile["send_command"] = str(profile.get("send_command") or "").strip()
        return profile

    def _create_default_project(self) -> None:
        profile = self._normalize_profile(None)
        profile["id"] = make_project_id(profile["name"])
        self.projects = [profile]
        self.selected_project_id = profile["id"]
        self._save()

    def _ensure_selected_project(self) -> None:
        if any(item.get("id") == self.selected_project_id for item in self.projects):
            return
        self.selected_project_id = str(self.projects[0].get("id") or "") if self.projects else ""

    def _load(self) -> None:
        if not self.store_path.exists():
            return
        try:
            payload = json.loads(self.store_path.read_text(encoding="utf-8"))
        except Exception:
            return
        self.selected_project_id = str(payload.get("selected_project_id") or "")
        self.projects = [self._normalize_profile(item) for item in list(payload.get("projects") or [])]

    def _dedupe_projects(self) -> bool:
        if len(self.projects) <= 1:
            return False
        selected_signature = ""
        selected_profile = self.project_profile(self.selected_project_id)
        if selected_profile is not None:
            selected_signature = project_signature(selected_profile)

        seen: set[str] = set()
        deduped_reversed: list[dict[str, Any]] = []
        signature_to_id: dict[str, str] = {}
        changed = False

        for profile in reversed(self.projects):
            signature = project_signature(profile)
            if signature in seen:
                changed = True
                continue
            seen.add(signature)
            signature_to_id[signature] = str(profile.get("id") or "")
            deduped_reversed.append(profile)

        if not changed:
            return False

        self.projects = list(reversed(deduped_reversed))
        if selected_signature:
            self.selected_project_id = signature_to_id.get(selected_signature, self.selected_project_id)
        self._ensure_selected_project()
        return True

    def _save(self) -> None:
        payload = {
            "selected_project_id": self.selected_project_id,
            "projects": self.projects,
        }
        self.store_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")

    def _watcher_store_path(self, project_id: str) -> pathlib.Path:
        return self.states_dir / f"{project_id}.json"

    def _make_watcher(self, profile: dict[str, Any]) -> GitWatchController:
        project_id = str(profile.get("id") or "")
        store_path = self._watcher_store_path(project_id)

        def on_trigger(trigger_info: dict[str, Any], settings: dict[str, Any], pid: str = project_id) -> None:
            if project_running(pid):
                return
            settings = dict(settings)
            settings["id"] = pid
            start_run_thread(settings, bool(settings.get("send_after_run", True)), trigger_info=trigger_info)

        watcher = GitWatchController(
            store_path,
            profile,
            on_trigger=on_trigger,
            is_job_running=lambda pid=project_id: project_running(pid),
        )
        watcher.sync_settings(profile)
        if watcher.snapshot().get("enabled"):
            watcher.start()
        return watcher

    def _rebuild_watchers(self) -> None:
        current_ids = {str(item.get("id") or "") for item in self.projects}
        stale_ids = [project_id for project_id in self.watchers if project_id not in current_ids]
        for project_id in stale_ids:
            try:
                self.watchers[project_id].stop()
            except Exception:
                pass
            del self.watchers[project_id]
        for path in self.states_dir.glob("*.json"):
            if path.stem in current_ids:
                continue
            try:
                path.unlink()
            except OSError:
                pass
        for profile in self.projects:
            project_id = str(profile.get("id") or "")
            watcher = self.watchers.get(project_id)
            if watcher is None:
                self.watchers[project_id] = self._make_watcher(profile)
            else:
                watcher.sync_settings(profile)

    def _find_index(self, project_id: str) -> int:
        for index, item in enumerate(self.projects):
            if str(item.get("id") or "") == project_id:
                return index
        return -1

    def project_profile(self, project_id: str) -> dict[str, Any] | None:
        index = self._find_index(project_id)
        if index < 0:
            return None
        return dict(self.projects[index])

    def _safe_watcher_snapshot(
        self,
        watcher: GitWatchController,
        fallback_settings: dict[str, Any] | None = None,
    ) -> dict[str, Any]:
        if watcher.lock.acquire(blocking=False):
            try:
                return {
                    "settings": dict(watcher.settings),
                    "enabled": watcher.enabled,
                    "checking": watcher.checking,
                    "last_check_at": watcher.last_check_at,
                    "last_error": watcher.last_error,
                    "watch_ref": watcher.watch_ref,
                    "current_head": watcher.current_head,
                    "last_seen_commit": watcher.last_seen_commit,
                    "last_triggered_commit": watcher.last_triggered_commit,
                    "last_triggered_at": watcher.last_triggered_at,
                    "last_match_files": list(watcher.last_match_files),
                    "pending_trigger": dict(watcher.pending_trigger) if watcher.pending_trigger else None,
                    "events": list(watcher.events),
                }
            finally:
                watcher.lock.release()
        settings = dict(fallback_settings or getattr(watcher, "settings", {}) or {})
        return {
            "settings": settings,
            "enabled": bool(getattr(watcher, "enabled", False)),
            "checking": bool(getattr(watcher, "checking", False)),
            "last_check_at": str(getattr(watcher, "last_check_at", "") or ""),
            "last_error": "状态读取中，请稍后刷新",
            "watch_ref": str(getattr(watcher, "watch_ref", "") or ""),
            "current_head": str(getattr(watcher, "current_head", "") or ""),
            "last_seen_commit": str(getattr(watcher, "last_seen_commit", "") or ""),
            "last_triggered_commit": str(getattr(watcher, "last_triggered_commit", "") or ""),
            "last_triggered_at": str(getattr(watcher, "last_triggered_at", "") or ""),
            "last_match_files": list(getattr(watcher, "last_match_files", []) or []),
            "pending_trigger": None,
            "events": list(getattr(watcher, "events", []) or []),
        }

    def current_profile(self) -> dict[str, Any]:
        profile = self.project_profile(self.selected_project_id)
        if profile is not None:
            return profile
        fallback = self._normalize_profile(None)
        fallback["id"] = ""
        return fallback

    def current_watcher(self) -> GitWatchController:
        project_id = self.selected_project_id
        if not project_id or project_id not in self.watchers:
            raise ValueError("当前没有可用的项目。")
        return self.watchers[project_id]

    def watcher_for(self, project_id: str = "") -> GitWatchController:
        resolved = self.resolve_project_id(project_id)
        if not resolved or resolved not in self.watchers:
            raise ValueError("当前没有可用的项目。")
        return self.watchers[resolved]

    def resolve_project_id(self, project_id: str = "") -> str:
        wanted = str(project_id or "").strip()
        if wanted and self._find_index(wanted) >= 0:
            return wanted
        if self.selected_project_id and self._find_index(self.selected_project_id) >= 0:
            return self.selected_project_id
        return str(self.projects[0].get("id") or "") if self.projects else ""

    def project_rows(self) -> list[dict[str, Any]]:
        rows: list[dict[str, Any]] = []
        for profile in self.projects:
            project_id = str(profile.get("id") or "")
            watcher = self.watchers.get(project_id)
            snapshot = self._safe_watcher_snapshot(watcher, profile) if watcher is not None else {}
            rows.append({
                "id": project_id,
                "name": str(profile.get("name") or "未命名项目"),
                "runner_kind": str(profile.get("runner_kind") or "wordgroup_builtin"),
                "git_remote": str(profile.get("git_remote") or ""),
                "git_branch": str(profile.get("git_branch") or ""),
                "enabled": bool(snapshot.get("enabled", False)),
                "checking": bool(snapshot.get("checking", False)),
                "last_error": str(snapshot.get("last_error") or ""),
                "last_check_at": str(snapshot.get("last_check_at") or ""),
                "last_triggered_at": str(snapshot.get("last_triggered_at") or ""),
                "watch_ref": str(snapshot.get("watch_ref") or ""),
                "current_head": str(snapshot.get("current_head") or ""),
            })
        return rows

    def create_project(self, payload: dict[str, Any] | None = None) -> dict[str, Any]:
        profile = self._normalize_profile(payload)
        profile["id"] = make_project_id(profile["name"])
        self.projects.append(profile)
        self.selected_project_id = profile["id"]
        self._save()
        self._rebuild_watchers()
        return self.app_state()

    def save_project(self, payload: dict[str, Any]) -> dict[str, Any]:
        profile = self._normalize_profile(payload)
        project_id = str(profile.get("id") or "").strip()
        if not project_id:
            profile["id"] = make_project_id(profile["name"])
            self.projects.append(profile)
        else:
            index = self._find_index(project_id)
            if index < 0:
                self.projects.append(profile)
            else:
                self.projects[index] = profile
        self.selected_project_id = str(profile.get("id") or "")
        self._save()
        self._rebuild_watchers()
        watcher = self.watchers[self.selected_project_id]
        watcher.update_settings(profile)
        return self.app_state()

    def select_project(self, project_id: str) -> dict[str, Any]:
        if self._find_index(project_id) < 0:
            raise ValueError("未找到对应项目。")
        self.selected_project_id = project_id
        self._save()
        return self.app_state()

    def delete_project(self, project_id: str) -> dict[str, Any]:
        index = self._find_index(project_id)
        if index < 0:
            raise ValueError("未找到对应项目。")
        watcher = self.watchers.get(project_id)
        if watcher is not None:
            watcher.stop()
            self.watchers.pop(project_id, None)
        state_path = self._watcher_store_path(project_id)
        if state_path.exists():
            try:
                state_path.unlink()
            except OSError:
                pass
        self.projects.pop(index)
        PROJECT_SUMMARIES.pop(project_id, None)
        if not self.projects:
            self._create_default_project()
        else:
            self._ensure_selected_project()
            if self.selected_project_id == project_id:
                self.selected_project_id = str(self.projects[0].get("id") or "")
        self._save()
        self._rebuild_watchers()
        return self.app_state()

    def selected_snapshot(self, project_id: str = "") -> dict[str, Any]:
        resolved = self.resolve_project_id(project_id)
        profile = self.project_profile(resolved) or self.current_profile()
        watcher = self.watcher_for(resolved)
        monitor = self._safe_watcher_snapshot(watcher, profile)
        settings = dict(monitor.get("settings") or profile)
        project_id = str(profile.get("id") or resolved or "")
        return {
            "profile": profile,
            "monitor": monitor,
            "summary": build_project_summary(settings, project_id),
            "diagnostics": build_project_diagnostics(settings),
        }

    def app_state(self, project_id: str = "", auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        resolved = self.resolve_project_id(project_id)
        selected = self.selected_snapshot(resolved)
        return {
            "job": job_state_for(resolved).snapshot(),
            "projects": self.project_rows(),
            "selected_project_id": resolved,
            "selected_project": selected,
            "summary": selected["summary"],
            "monitor": selected["monitor"],
            "diagnostics": selected["diagnostics"],
            "auth": auth_user or {},
        }

    def start_selected(self, project_id: str = "", auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        self.watcher_for(project_id).start()
        return self.app_state(project_id, auth_user=auth_user)

    def stop_selected(self, project_id: str = "", auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        self.watcher_for(project_id).stop()
        return self.app_state(project_id, auth_user=auth_user)

    def check_selected(self, project_id: str = "", auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        self.watcher_for(project_id).request_check()
        return self.app_state(project_id, auth_user=auth_user)

    def baseline_selected(self, project_id: str = "", auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        self.watcher_for(project_id).set_baseline_to_current()
        return self.app_state(project_id, auth_user=auth_user)

    def start_all(self, auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        for watcher in self.watchers.values():
            watcher.start()
        return self.app_state(auth_user=auth_user)

    def stop_all(self, auth_user: dict[str, Any] | None = None) -> dict[str, Any]:
        for watcher in self.watchers.values():
            watcher.stop()
        return self.app_state(auth_user=auth_user)


