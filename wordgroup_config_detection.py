def run_shell_command(
    command: str,
    settings: dict[str, Any],
    trigger_info: dict[str, Any] | None = None,
    state: JobState | None = None,
) -> int:
    state = state or STATE
    rendered = command.format_map(SafeFormatDict(command_context(settings, trigger_info)))
    cwd = str(settings.get("git_repo_path") or settings.get("project_path") or pathlib.Path.cwd())
    env = os.environ.copy()
    env.update({
        "MONITOR_PROJECT_ID": str(settings.get("id") or ""),
        "MONITOR_PROJECT_NAME": str(settings.get("name") or settings.get("project_name") or ""),
        "MONITOR_GIT_REPO_PATH": str(settings.get("git_repo_path") or ""),
        "MONITOR_PROJECT_PATH": str(settings.get("project_path") or ""),
        "MONITOR_GIT_BRANCH": str(settings.get("git_branch") or ""),
        "MONITOR_GIT_REMOTE": str(settings.get("git_remote") or ""),
    })
    state.add_log_event("运行", "命令：" + rendered)
    state.add_log_event("运行", "工作目录：" + cwd)
    timeout = int(settings.get("process_timeout_seconds") or settings.get("timeout_seconds") or 1800)
    if timeout <= 0:
        timeout = 1800
    proc = subprocess.Popen(
        ["/bin/zsh", "-lc", rendered],
        cwd=cwd,
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        bufsize=1,
    )
    deadline = time.monotonic() + timeout
    try:
        if proc.stdout is not None:
            for line in iter(proc.stdout.readline, ""):
                if line:
                    state.add_log_event("运行", line)
                if proc.poll() is not None and not line:
                    break
                if time.monotonic() > deadline:
                    proc.terminate()
                    raise TimeoutError(f"命令执行超时：{timeout}s")
        return int(proc.wait(timeout=max(1, int(deadline - time.monotonic()))))
    finally:
        if proc.stdout is not None:
            try:
                proc.stdout.close()
            except Exception:
                pass


