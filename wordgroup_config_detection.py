def derive_project_name(settings: dict[str, Any]) -> str:
    for key in ("project_name", "name"):
        value = str(settings.get(key) or "").strip()
        if value:
            return value
    for key in ("git_repo_path", "project_path"):
        value = str(settings.get(key) or "").strip()
        if value:
            return pathlib.Path(value).expanduser().name or "未命名项目"
    return "未命名项目"


def make_project_id(name: str) -> str:
    base = "".join(ch.lower() if ch.isalnum() else "-" for ch in name).strip("-")
    if not base:
        base = "project"
    return f"{base}-{int(time.time() * 1000)}"


def project_signature(profile: dict[str, Any]) -> str:
    payload = {
        key: value
        for key, value in profile.items()
        if key not in {"id"}
    }
    return json.dumps(payload, ensure_ascii=False, sort_keys=True)


def normalize_auth_role(value: str) -> str:
    role = str(value or "").strip().lower()
    if role in {"admin", "manager", "write", "rw"}:
        return "admin"
    return "readonly"


def load_ui_users(args: argparse.Namespace) -> dict[str, dict[str, str]]:
    cached = getattr(args, "_ui_users_cache", None)
    if isinstance(cached, dict):
        return cached

    users: dict[str, dict[str, str]] = {}
    users_file = str(getattr(args, "ui_users_file", "") or "").strip()
    if users_file:
        path = pathlib.Path(users_file).expanduser()
        if path.is_file():
            try:
                payload = json.loads(path.read_text(encoding="utf-8"))
            except Exception:
                payload = {}
            raw_users = payload.get("users") if isinstance(payload, dict) else []
            if isinstance(raw_users, list):
                for item in raw_users:
                    if not isinstance(item, dict):
                        continue
                    username = str(item.get("username") or "").strip()
                    password = str(item.get("password") or "")
                    if not username or not password:
                        continue
                    users[username] = {
                        "password": password,
                        "role": normalize_auth_role(str(item.get("role") or "readonly")),
                    }

    username = str(getattr(args, "ui_username", "") or "").strip()
    password = str(getattr(args, "ui_password", "") or "")
    if username and password and username not in users:
        users[username] = {
            "password": password,
            "role": "admin",
        }

    args._ui_users_cache = users
    return users


