def git_output(repo_path: pathlib.Path, args: list[str], timeout: int = 15) -> str:
    completed = subprocess.run(
        ["git", "-C", str(repo_path), *args],
        check=True,
        capture_output=True,
        text=True,
        timeout=timeout,
    )
    return completed.stdout.strip()


def detect_git_watch_target(repo_path: pathlib.Path) -> dict[str, str]:
    repo_path = repo_path.expanduser().resolve()
    result = {"git_remote": "", "git_branch": "main"}
    if not repo_path.is_dir() or not (repo_path / ".git").exists():
        return result
    try:
        branch = git_output(repo_path, ["rev-parse", "--abbrev-ref", "HEAD"])
        if branch and branch != "HEAD":
            result["git_branch"] = branch
    except Exception:
        return result
    try:
        upstream = git_output(repo_path, ["rev-parse", "--abbrev-ref", "--symbolic-full-name", "@{upstream}"])
    except Exception:
        return result
    if not upstream or upstream == "HEAD":
        return result
    if "/" in upstream:
        remote, branch = upstream.split("/", 1)
        result["git_remote"] = remote.strip()
        result["git_branch"] = branch.strip() or result["git_branch"]
    return result


def git_sync_command(repo_path: pathlib.Path, args: list[str], timeout: int = 300) -> str:
    env = dict(os.environ)
    env["GIT_TERMINAL_PROMPT"] = "0"
    env["GCM_INTERACTIVE"] = "Never"
    completed = subprocess.run(
        ["git", "-C", str(repo_path), *args],
        check=True,
        capture_output=True,
        text=True,
        timeout=timeout,
        env=env,
    )
    return completed.stdout.strip()


def is_unity_project(path: pathlib.Path) -> bool:
    return (path / "Assets").is_dir() and (path / "ProjectSettings").is_dir()


def project_score(path: pathlib.Path) -> int:
    score = 0
    normalized = str(path).lower()
    if "wordgroup" in normalized:
        score += 50
    if (path / "Assets" / "Editor" / "Tools" / "TEST" / "TEST_ConfigDetectTool.cs").is_file():
        score += 100
    if (path / "ProjectSettings" / "ProjectVersion.txt").is_file():
        score += 10
    return score


def iter_project_candidates(seed: pathlib.Path) -> list[pathlib.Path]:
    roots: list[pathlib.Path] = []

    def add_root(value: str | pathlib.Path | None) -> None:
        if not value:
            return
        try:
            path = pathlib.Path(value).expanduser().resolve()
        except OSError:
            return
        if path not in roots:
            roots.append(path)

    add_root(seed)
    add_root(pathlib.Path.cwd())
    add_root(pathlib.Path(__file__).resolve().parent)
    add_root(os.environ.get("WORKSPACE"))
    add_root(os.environ.get("JENKINS_HOME"))

    for root in list(roots):
        for parent in list(root.parents)[:3]:
            add_root(parent)

    direct_names = ("", "WordGroup", "wordgroup", "WordGroup/WordGroup", "wordgroup/WordGroup")
    candidates: list[pathlib.Path] = []
    seen: set[pathlib.Path] = set()

    def add_candidate(path: pathlib.Path) -> None:
        try:
            resolved = path.expanduser().resolve()
        except OSError:
            return
        if resolved not in seen:
            seen.add(resolved)
            candidates.append(resolved)

    for root in roots:
        for name in direct_names:
            add_candidate(root / name if name else root)

    for root in roots:
        if not root.is_dir():
            continue
        stack = [(root, 0)]
        while stack:
            current, depth = stack.pop()
            if depth > 4:
                continue
            if is_unity_project(current):
                add_candidate(current)
                continue
            try:
                children = list(current.iterdir())
            except OSError:
                continue
            for child in children:
                if not child.is_dir() or child.name in {".git", "Library", "Temp", "Obj", "Build", "Builds", "Logs"}:
                    continue
                stack.append((child, depth + 1))

    return candidates


def resolve_project_path(project_path: pathlib.Path) -> pathlib.Path:
    project_path = project_path.expanduser().resolve()
    if is_unity_project(project_path):
        return project_path

    valid = [path for path in iter_project_candidates(project_path) if is_unity_project(path)]
    if valid:
        return sorted(valid, key=lambda item: (-project_score(item), len(str(item))))[0]

    return project_path


def read_project_unity_version(project_path: pathlib.Path) -> str:
    version_path = project_path / "ProjectSettings" / "ProjectVersion.txt"
    if not version_path.is_file():
        return ""
    try:
        for line in version_path.read_text(encoding="utf-8", errors="replace").splitlines():
            if line.startswith("m_EditorVersion:"):
                return line.split(":", 1)[1].strip()
    except OSError:
        return ""
    return ""


def base_unity_version(version: str) -> str:
    if not version:
        return ""
    marker = version.find("c")
    return version[:marker] if marker > 0 else version


def unity_version_series(version: str) -> str:
    base_version = base_unity_version(version)
    if not base_version:
        return ""
    parts = base_version.split(".")
    if len(parts) >= 2:
        return ".".join(parts[:2])
    return base_version


def unity_candidate_score(path: pathlib.Path, expected_version: str) -> tuple[int, str]:
    text = str(path)
    score = 0
    if expected_version and f"/{expected_version}/" in text:
        score += 100
    else:
        base_version = base_unity_version(expected_version)
        if base_version and f"/{base_version}/" in text:
            score += 60
        else:
            version_series = unity_version_series(expected_version)
            if version_series and f"/{version_series}." in text:
                score += 40
    if "/Applications/Unity/Hub/Editor/" in text:
        score += 20
    return (-score, text)


def find_unity(explicit_path: str | None = None, project_path: pathlib.Path | None = None) -> str:
    expected_version = read_project_unity_version(project_path) if project_path else ""
    base_version = base_unity_version(expected_version)
    version_series = unity_version_series(expected_version)
    candidates = []
    if explicit_path:
        candidates.append(explicit_path)
    env_path = os.environ.get("UNITY_PATH")
    if env_path:
        candidates.append(env_path)
    if expected_version:
        candidates.append(f"/Applications/Unity/Hub/Editor/{expected_version}/Unity.app/Contents/MacOS/Unity")
    candidates.extend(DEFAULT_UNITY_CANDIDATES)
    candidates.extend(str(path) for path in pathlib.Path("/Applications/Unity/Hub/Editor").glob("*/Unity.app/Contents/MacOS/Unity"))
    candidates.extend(str(path) for path in pathlib.Path("/Applications").glob("Unity*.app/Contents/MacOS/Unity"))

    unique_candidates = []
    seen_candidates = set()
    for candidate in candidates:
        if not candidate:
            continue
        path = pathlib.Path(candidate)
        if path in seen_candidates:
            continue
        seen_candidates.add(path)
        unique_candidates.append(path)

    fallback_path = None
    for path in sorted(unique_candidates, key=lambda item: unity_candidate_score(item, expected_version)):
        if path.is_file():
            text = str(path)
            if expected_version and f"/{expected_version}/" in text:
                return text
            if expected_version and base_version and f"/{base_version}/" in text and fallback_path is None:
                fallback_path = text
            elif expected_version and version_series and f"/{version_series}." in text and fallback_path is None:
                fallback_path = text
            elif not expected_version:
                return text

    if fallback_path:
        return fallback_path

    found = shutil.which("Unity")
    if found and (
        not expected_version
        or expected_version in found
        or (base_version and base_version in found)
        or (version_series and version_series in found)
    ):
        return found
    version_hint = (
        f"项目需要 Unity {expected_version}（或同属 {version_series} 的兼容版本）。"
        if expected_version else ""
    )
    raise FileNotFoundError(
        "未找到 Unity 可执行文件。"
        f"{version_hint}\n"
        "请在 Jenkins 中传入实际 Unity 路径，例如：\n"
        f"  python3 wordgroup_config_detection.py run --send --unity-path \"/Applications/Unity/Hub/Editor/{base_version or version_series or '2022.3.49f1'}/Unity.app/Contents/MacOS/Unity\"\n"
        "或设置环境变量：\n"
        f"  export UNITY_PATH=\"/Applications/Unity/Hub/Editor/{base_version or version_series or '2022.3.49f1'}/Unity.app/Contents/MacOS/Unity\""
    )


