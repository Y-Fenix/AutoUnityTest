def ensure_share_auth(args: argparse.Namespace) -> None:
    host = str(getattr(args, "host", "127.0.0.1") or "127.0.0.1").strip()
    if host in {"127.0.0.1", "localhost"}:
        return
    if auth_enabled(args):
        return
    if not getattr(args, "insecure_no_auth", False):
        raise ValueError("局域网共享模式必须配置用户权限文件，或设置 --ui-username/--ui-password，或显式传入 --insecure-no-auth。")


def resolve_share_auth(args: argparse.Namespace) -> None:
    host = str(getattr(args, "host", "127.0.0.1") or "127.0.0.1").strip()
    if host in {"127.0.0.1", "localhost"}:
        return
    if load_ui_users(args):
        return
    username = str(getattr(args, "ui_username", "") or "").strip()
    password = str(getattr(args, "ui_password", "") or "").strip()
    if username and password:
        return
    if getattr(args, "insecure_no_auth", False):
        return
    args.ui_username = "viewer"
    args.ui_password = secrets.token_urlsafe(12)
    setattr(args, "_ui_users_cache", None)


def serve_ui(args: argparse.Namespace) -> None:
    resolve_share_auth(args)
    ensure_share_auth(args)
    app = ensure_app(args)
    host = ui_host_value(args)
    bind_host = "" if host == "0.0.0.0" else host
    port = ensure_port_available(host, int(args.port))
    server = ThreadingHTTPServer((bind_host, port), WebHandler)
    server.args = args  # type: ignore[attr-defined]
    local_url = f"http://127.0.0.1:{port}/"
    print(f"WordGroup 配置检测面板（本机）：{local_url}")
    if host not in {"127.0.0.1", "localhost"}:
        lan_urls = []
        if host == "0.0.0.0":
            lan_urls = [f"http://{ip}:{port}/" for ip in local_ipv4_addresses()]
        else:
            lan_urls = [f"http://{host}:{port}/"]
        if lan_urls:
            print("局域网访问地址：")
            for item in lan_urls:
                print("  " + item)
        users = load_ui_users(args)
        if users:
            print("访问权限：")
            for username, item in users.items():
                print(f"  {username} ({item.get('role')})")
                print(f"    password: {item.get('password')}")
        elif getattr(args, "insecure_no_auth", False):
            print("警告：当前以无认证模式对局域网开放。")
    try:
        diagnostics = build_project_diagnostics(app.selected_snapshot()["monitor"]["settings"])
        for line in format_diagnostics(diagnostics).splitlines():
            STATE.add_log_event("自检", line)
    except Exception as exc:
        STATE.add_log_event("错误", "自检失败：" + str(exc))
    if not args.no_open:
        webbrowser.open(local_url)
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\n已关闭。")


def build_parser() -> argparse.ArgumentParser:
    common = argparse.ArgumentParser(add_help=False)
    common.add_argument(
        "--project-path",
        default=str(DEFAULT_PROJECT_PATH),
        help="Unity 工程路径；Jenkins 推荐用 WORDGROUP_PROJECT_PATH 或 --project-path 传入",
    )
    common.add_argument("--unity-path", default=None, help="Unity 可执行文件路径；也可使用 UNITY_PATH 环境变量")
    common.add_argument("--webhook", default=os.environ.get("FEISHU_WEBHOOK", DEFAULT_WEBHOOK), help="飞书机器人 webhook")
    common.add_argument("--timeout-seconds", type=int, default=1800, help="单项检测超时时间")
    common.add_argument(
        "--process-timeout-seconds",
        type=int,
        default=0,
        help="Unity 进程总超时时间；0 表示按检测项数量自动计算",
    )
