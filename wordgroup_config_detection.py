    ui_common.add_argument("--port", type=int, default=8765, help="本地面板端口")
    ui_common.add_argument("--host", default="127.0.0.1", help="监听地址；局域网共享可用 0.0.0.0 或指定本机局域网 IP")
    ui_common.add_argument("--share", action="store_true", help="快捷开启局域网共享模式，等价于 --host 0.0.0.0")
