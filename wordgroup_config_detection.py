def job_state_for(project_id: str = "", project_name: str = "", runner_kind: str = "wordgroup_builtin") -> JobState:
    resolved = str(project_id or "").strip()
    if not resolved:
        return STATE
    with PROJECT_JOB_STATES_LOCK:
        state = PROJECT_JOB_STATES.get(resolved)
        if state is None:
            state = JobState()
            PROJECT_JOB_STATES[resolved] = state
        if project_name and not state.project_name:
            state.project_name = project_name
        if runner_kind and not state.runner_kind:
            state.runner_kind = runner_kind
        return state


