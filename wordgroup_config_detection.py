def installed_service_config() -> dict[str, Any]:
    if not SERVICE_PLIST_PATH.exists():
        return {}
    try:
        with SERVICE_PLIST_PATH.open("rb") as fh:
            plist = plistlib.load(fh)
    except Exception:
        return {}
    argv = list(plist.get("ProgramArguments") or [])
    parsed: dict[str, Any] = {
        "label": str(plist.get("Label") or SERVICE_LABEL),
        "program_arguments": argv,
        "stdout_path": str(plist.get("StandardOutPath") or ""),
        "stderr_path": str(plist.get("StandardErrorPath") or ""),
    }
    for index, item in enumerate(argv[:-1]):
        if item == "--port":
            parsed["port"] = str(argv[index + 1])
        elif item == "--host":
            parsed["host"] = str(argv[index + 1])
        elif item == "--project-path":
            parsed["project_path"] = str(argv[index + 1])
    return parsed


def launchctl_domain() -> str:
    return f"gui/{os.getuid()}"


def launchctl_service_target() -> str:
    return f"{launchctl_domain()}/{SERVICE_LABEL}"


def run_launchctl(arguments: list[str], check: bool = True) -> subprocess.CompletedProcess[str]:
    completed = subprocess.run(
        ["launchctl", *arguments],
        capture_output=True,
        text=True,
    )
    if check and completed.returncode != 0:
        detail = (completed.stderr or completed.stdout or "").strip()
        raise RuntimeError(detail or f"launchctl {' '.join(arguments)} 失败")
    return completed


def stop_background_service(ignore_errors: bool = True) -> None:
    errors: list[str] = []
    for arguments in (
        ["bootout", launchctl_domain(), str(SERVICE_PLIST_PATH)],
        ["bootout", launchctl_service_target()],
    ):
        completed = run_launchctl(arguments, check=False)
        if completed.returncode == 0:
            return
        detail = (completed.stderr or completed.stdout or "").strip()
        if detail:
            errors.append(detail)
    if not ignore_errors and errors:
        raise RuntimeError(errors[-1])


def service_matches_args(status: dict[str, Any], args: argparse.Namespace) -> bool:
    expected_port = str(int(getattr(args, "port", 8765) or 8765))
    expected_host = ui_host_value(args)
    expected_project_path = str(getattr(args, "project_path", "") or "").strip()
    actual_project_path = str(status.get("project_path") or "").strip()
    if str(status.get("port") or "") != expected_port:
        return False
    if str(status.get("host") or "") != expected_host:
        return False
    if expected_project_path != actual_project_path:
        return False
    return True


def install_background_service(args: argparse.Namespace) -> dict[str, Any]:
    stop_background_service(ignore_errors=True)
    ensure_port_available(ui_host_value(args), int(getattr(args, "port", 8765) or 8765))
    SERVICE_PLIST_PATH.parent.mkdir(parents=True, exist_ok=True)
    plist = build_service_plist(args)
    with SERVICE_PLIST_PATH.open("wb") as fh:
        plistlib.dump(plist, fh, sort_keys=False)
    run_launchctl(["bootstrap", launchctl_domain(), str(SERVICE_PLIST_PATH)])
    run_launchctl(["kickstart", "-k", launchctl_service_target()])
    return installed_service_config()


def background_service_status() -> dict[str, Any]:
    config = installed_service_config()
    completed = run_launchctl(["print", launchctl_service_target()], check=False)
    output = (completed.stdout or "") + ("\n" + completed.stderr if completed.stderr else "")
    pid_match = re.search(r"\bpid = (\d+)\b", output)
    state_match = re.search(r"\bstate = (\w+)\b", output)
    config.update(
        {
            "installed": SERVICE_PLIST_PATH.exists(),
            "loaded": completed.returncode == 0,
            "pid": int(pid_match.group(1)) if pid_match else 0,
            "state": state_match.group(1) if state_match else "",
            "raw": output.strip(),
        }
    )
    port_text = str(config.get("port") or "").strip()
    port = int(port_text) if port_text.isdigit() else 8765
    config["local_url"] = ui_local_url(port)
    return config


def wait_for_service_http(port: int, timeout_seconds: float = 8.0) -> bool:
    deadline = time.time() + max(1.0, timeout_seconds)
    while time.time() < deadline:
        try:
            with socket.create_connection(("127.0.0.1", int(port)), timeout=1.0):
                return True
        except Exception:
            time.sleep(0.25)
    return False


def ensure_background_service(args: argparse.Namespace) -> tuple[dict[str, Any], bool]:
    status = background_service_status()
    if status.get("loaded") and service_matches_args(status, args):
        return status, False
    return install_background_service(args), True


