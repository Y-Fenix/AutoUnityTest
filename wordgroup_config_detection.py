def detect_project_lock_owner(project_path: pathlib.Path) -> str:
    lockfile = project_path / "Temp" / "UnityLockfile"
    if not lockfile.exists():
        return ""
    commands = (
        ["/usr/sbin/lsof", str(lockfile)],
        ["lsof", str(lockfile)],
    )
    for cmd in commands:
        try:
            completed = subprocess.run(
                cmd,
                check=False,
                capture_output=True,
                text=True,
                timeout=5,
            )
        except Exception:
            continue
        lines = [line.strip() for line in (completed.stdout or "").splitlines() if line.strip()]
        if len(lines) <= 1:
            continue
        details: list[str] = []
        for line in lines[1:4]:
            columns = re.split(r"\s+", line)
            if not columns:
                continue
            command = columns[0]
            pid = columns[1] if len(columns) > 1 else "?"
            user = columns[2] if len(columns) > 2 else "?"
            details.append(f"{command}(pid={pid}, user={user})")
        if details:
            return "，".join(details)
    return ""


def supports_editor_bridge(settings: dict[str, Any] | None) -> bool:
    if not settings:
        return False
    haystack = " ".join([
        str(settings.get("name") or ""),
        str(settings.get("project_name") or ""),
        str(settings.get("project_path") or ""),
        str(settings.get("git_repo_path") or ""),
    ]).lower()
    return any(preset["match"] in haystack for preset in BUILTIN_METHOD_PRESETS)


def run_editor_bridge_checks(
    project_path: pathlib.Path,
    timeout_seconds: int,
    settings: dict[str, Any],
    state: JobState | None = None,
) -> int:
    state = state or STATE
    project_path = resolve_project_path(project_path)
    ensure_editor_bridge(project_path, settings=settings)
    result_dir = repo_root_from_project(project_path) / "Result"
    result_dir.mkdir(parents=True, exist_ok=True)
    request_path = result_dir / "wordgroup_editor_bridge_request.json"
    status_path = result_dir / "wordgroup_editor_bridge_status.json"
    request_id = secrets.token_hex(8)

    for path in (request_path, status_path):
        try:
            if path.exists():
                path.unlink()
        except OSError:
            pass

    request_payload = {
        "requestId": request_id,
        "timeoutSeconds": int(timeout_seconds),
    }
    request_path.write_text(json.dumps(request_payload, ensure_ascii=False, indent=2), encoding="utf-8")
    state.add_log_event("桥接", "检测到工程已在 Unity 中打开，改走编辑器内执行。")
    state.add_log_event("桥接", "请求文件：" + str(request_path))

    last_snapshot = ""
    started_at = time.monotonic()
    deadline = time.monotonic() + max(120, timeout_seconds * (len(build_checks(settings)) + 1))
    while time.monotonic() <= deadline:
        if status_path.exists():
            try:
                payload = json.loads(status_path.read_text(encoding="utf-8"))
            except Exception:
                payload = {}
            if str(payload.get("requestId") or "") == request_id:
                snapshot = json.dumps(payload, ensure_ascii=False, sort_keys=True)
                if snapshot != last_snapshot:
                    last_snapshot = snapshot
                    current_name = str(payload.get("currentCheckName") or "")
                    message = str(payload.get("message") or "")
                    state = str(payload.get("state") or "")
                    updated_at = str(payload.get("updatedAt") or "")
                    detail = state
                    if current_name:
                        detail += f" {current_name}"
                    if message:
                        detail += f" - {message}"
                    when = ""
                    if updated_at:
                        parts = updated_at.split(" ")
                        when = parts[-1] if parts else ""
                    state.add_log_event("桥接", detail, when=when or None)
                state = str(payload.get("state") or "")
                if state == "completed":
                    return 0
                if state == "failed":
                    raise RuntimeError(str(payload.get("message") or "编辑器内检测失败。"))
        elif time.monotonic() - started_at > 20:
            raise RuntimeError(
                "已切换到编辑器内执行，但 Unity 编辑器 20 秒内没有接收到桥接状态。\n"
                f"请求文件：{request_path}\n"
                "常见原因：\n"
                "1. 当前打开的 Unity 工程关闭了 Auto Refresh，尚未编译新生成的桥接脚本；\n"
                "2. Unity 正在忙于编译/导入资源；\n"
                "3. 首次接入时，需要在已打开的 Unity 中手动执行一次 Assets > Refresh。\n"
                "如果你希望工具以后自动点 Refresh，需要先给运行它的终端/应用开启 macOS 辅助功能权限。"
            )
        time.sleep(1)

    raise TimeoutError("等待编辑器内检测结果超时。请确认 Unity 编辑器处于空闲状态并已完成脚本编译。")


def stream_pipe_to_state(pipe: Any, state: JobState | None = None) -> None:
    state = state or STATE
    try:
        for line in iter(pipe.readline, ""):
            state.add_log_event("Unity", line)
    except Exception as exc:
        state.add_log_event("错误", f"读取 Unity stdout 失败：{exc}")
    finally:
        try:
            pipe.close()
        except Exception:
            pass


def write_failure_report(
    result_dir: pathlib.Path,
    check_key: str,
    message: str,
    settings: dict[str, Any] | None = None,
) -> pathlib.Path | None:
    check = next((item for item in build_checks(settings) if item["key"] == check_key), None)
    if check is None:
        return None
    report_path = result_dir / check["report"]
    content = "\n".join([
        f"WordGroup【{check['name']}】报告，{datetime.now():%y/%m/%d-%H:%M:%S}",
        "-" * 80,
        "问题汇总",
        f"执行失败：{message}",
        "-" * 80,
        "",
        "问题详情",
        message,
        "",
    ])
    report_path.write_text(content, encoding="utf-8-sig")
    return report_path


def tail_unity_log(
    log_path: pathlib.Path,
    stop_event: threading.Event,
    monitor: RunMonitor,
    settings: dict[str, Any] | None = None,
    state: JobState | None = None,
) -> None:
    state = state or STATE
    position = 0
    announced_missing = False
    important_words = (
        "WordGroupConfigDetectionBatchRunner",
        "error",
        "exception",
        "failed",
        "aborting",
        "licens",
        "compile",
        "reload",
        "import",
        "检测",
        "报告",
    )
    current_check_by_name = {item["name"]: item["key"] for item in build_checks(settings)}

    while not stop_event.is_set():
        if not log_path.exists():
            if not announced_missing:
                state.add_log_event("Unity", "等待 Unity 日志文件生成...")
                announced_missing = True
            time.sleep(1)
            continue

        try:
            size = log_path.stat().st_size
            if size < position:
                position = 0
            with log_path.open("r", encoding="utf-8", errors="replace") as handle:
                handle.seek(position)
                chunk = handle.read()
                position = handle.tell()
        except OSError as exc:
            state.add_log_event("错误", f"读取 Unity 日志失败：{exc}")
            time.sleep(1)
            continue

        for raw_line in chunk.splitlines():
            line = raw_line.strip()
            if not line:
                continue
            lower = line.lower()
            if "[WordGroupConfigDetectionBatchRunner] start " in line:
                name = line.split("start ", 1)[1].strip()
                monitor.set_current_check(current_check_by_name.get(name, ""))
                monitor.mark_progress(line)
            elif "[WordGroupConfigDetectionBatchRunner] completed " in line:
                monitor.set_current_check("")
                monitor.mark_progress(line)

            if "DisplayProgressbar:" in line or "[ImageDetection] collect " in line:
                monitor.mark_progress(line)

            if "scripts have compiler errors." in lower:
                monitor.set_fatal("Scripts have compiler errors.")
            elif "aborting batchmode due to failure" in lower:
                monitor.set_fatal("Aborting batchmode due to failure.")
            elif "argumentnullexception" in lower:
                monitor.set_fatal(line)
            elif "nullreferenceexception" in lower:
                monitor.set_fatal(line)
            elif "internal_logexception" in lower and monitor.get_current_check():
                monitor.set_fatal("Unity editor task raised an exception.")

            if any(signature in line for signature in IMAGE_STUCK_SIGNATURES):
                monitor.note_image_stuck_line(line)

            if any(word in lower for word in important_words):
                state.add_log_event("Unity", line)

        time.sleep(1)


def run_unity_checks(
    project_path: pathlib.Path,
    unity_path: str | None = None,
    timeout_seconds: int = 1800,
    process_timeout_seconds: int | None = None,
    keep_runner: bool = False,
    settings: dict[str, Any] | None = None,
    state: JobState | None = None,
) -> int:
    state = state or STATE
    project_path = resolve_project_path(project_path)
    if not (project_path / "Assets").is_dir():
        raise FileNotFoundError(
            "Unity 工程路径无效，未找到 Assets："
            f"{project_path}\n"
            "请在 Jenkins 中传入实际 Unity 工程目录，例如：\n"
            "  python3 wordgroup_config_detection.py run --send --project-path \"$WORKSPACE/WordGroup\"\n"
            "或设置环境变量：\n"
            "  export WORDGROUP_PROJECT_PATH=\"$WORKSPACE/WordGroup\""
        )

    lock_owner = detect_project_lock_owner(project_path)
    if lock_owner:
        if supports_editor_bridge(settings):
            state.add_log_event("Unity", f"检测到 Unity 工程正在被占用：{lock_owner}")
            return run_editor_bridge_checks(project_path, timeout_seconds, settings or {}, state=state)
        raise RuntimeError(
            "检测前发现该 Unity 工程已被打开，当前无法以 batchmode 再启动同一个工程。\n"
            f"工程：{project_path}\n"
            f"占用信息：{lock_owner}\n"
            "请先关闭这个工程对应的 Unity 编辑器后再试。"
        )

    unity = find_unity(unity_path, project_path)
    expected_version = read_project_unity_version(project_path)
    runner_path = ensure_batch_runner(project_path, settings=settings)
    result_dir = repo_root_from_project(project_path) / "Result"
    log_path = result_dir / "wordgroup_config_detection_unity.log"
    result_dir.mkdir(parents=True, exist_ok=True)

    cmd = [
        unity,
        "-batchmode",
        "-projectPath",
        str(project_path),
        "-executeMethod",
        "WordGroupConfigDetectionBatchRunner.RunAllAndQuit",
        "-logFile",
        str(log_path),
        "--timeout-seconds",
        str(timeout_seconds),
    ]

    state.add_log_event("Unity", unity)
    if expected_version and f"/{expected_version}/" not in unity:
        state.add_log_event("Unity", f"版本回退：期望 {expected_version}，实际使用 {unity}")
    state.add_log_event("运行", "工程：" + str(project_path))
    state.add_log_event("运行", "日志：" + str(log_path))
    checks = build_checks(settings)
    if process_timeout_seconds is None or process_timeout_seconds <= 0:
        process_timeout_seconds = max(1800, timeout_seconds * (len(checks) + 1))
    state.add_log_event("Unity", f"进程总超时设置：{process_timeout_seconds}s")

    if log_path.exists():
        try:
            log_path.unlink()
        except OSError:
            pass

    proc = subprocess.Popen(
        cmd,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        bufsize=1,
    )

    stop_event = threading.Event()
    monitor = RunMonitor()
    threads: list[threading.Thread] = []
    if proc.stdout is not None:
        stdout_thread = threading.Thread(target=stream_pipe_to_state, args=(proc.stdout, state), daemon=True)
        stdout_thread.start()
        threads.append(stdout_thread)
    log_thread = threading.Thread(target=tail_unity_log, args=(log_path, stop_event, monitor, settings, state), daemon=True)
    log_thread.start()
    threads.append(log_thread)

    deadline = time.monotonic() + process_timeout_seconds
    code: int | None = None
    while True:
        code = proc.poll()
        if code is not None:
            break
        fatal_message = monitor.get_fatal()
        if fatal_message:
            current_check = monitor.get_current_check()
            state.add_log_event("错误", f"检测期间捕获异常，准备终止 Unity：{fatal_message}")
            proc.terminate()
            try:
                code = proc.wait(timeout=30)
            except subprocess.TimeoutExpired:
                state.add_log_event("错误", "Unity 未响应 terminate，强制 kill。")
                proc.kill()
                code = proc.wait()
            if current_check:
                report_path = write_failure_report(result_dir, current_check, fatal_message, settings=settings)
                if report_path is not None:
                    state.add_log_event("运行", f"已写入失败报告：{report_path}")
            break
        if time.monotonic() > deadline:
            current_check = monitor.get_current_check()
            timeout_message = f"Unity 进程超过 {process_timeout_seconds}s 未结束，准备终止。"
            if current_check:
                timeout_message = f"{timeout_message} 当前检测：{current_check}"
            state.add_log_event("错误", timeout_message)
            proc.terminate()
            try:
                code = proc.wait(timeout=30)
            except subprocess.TimeoutExpired:
                state.add_log_event("错误", "Unity 未响应 terminate，强制 kill。")
                proc.kill()
                code = proc.wait()
            if current_check:
                report_path = write_failure_report(
                    result_dir,
                    current_check,
                    f"执行超时：{current_check} 超过 {process_timeout_seconds}s 未结束",
                    settings=settings,
                )
                if report_path is not None:
                    state.add_log_event("运行", f"已写入超时失败报告：{report_path}")
            break
        time.sleep(1)

    stop_event.set()
    for thread in threads:
        thread.join(timeout=2)

    if log_path.exists():
        try:
            tail = log_path.read_text(encoding="utf-8", errors="replace").splitlines()[-80:]
            for line in tail:
                if "WordGroupConfigDetectionBatchRunner" in line or "error" in line.lower():
                    state.add_log_event("Unity", line)
        except OSError:
            pass

    if (code or 0) != 0:
        fatal_message = monitor.get_fatal()
        current_check = monitor.get_current_check()
        if fatal_message and current_check:
            report_path = result_dir / next(item["report"] for item in checks if item["key"] == current_check)
            if not report_path.exists():
                write_failure_report(result_dir, current_check, fatal_message, settings=settings)

    if not keep_runner:
        cleanup_batch_runner(runner_path)
    return int(code or 0)


def read_text(path: pathlib.Path) -> str:
    return path.read_text(encoding="utf-8-sig", errors="replace")


def extract_summary(report_path: pathlib.Path, fallback_name: str) -> dict[str, Any]:
    if not report_path.exists():
        return {
            "name": fallback_name,
            "report": str(report_path),
            "exists": False,
            "header": "",
            "summary_lines": ["报告不存在"],
        }

    text = read_text(report_path)
    lines = text.splitlines()
    header = lines[0].strip() if lines else ""

    summary_lines: list[str] = []
    in_summary = False
    for raw in lines:
        line = raw.strip()
        if line == "问题汇总":
            in_summary = True
            continue
        if in_summary and set(line) == {"-"} and len(line) >= 8:
            break
        if in_summary:
            if line:
                summary_lines.append(line)

    if not summary_lines:
        summary_lines = ["未找到问题汇总段"]

    stat = report_path.stat()
    return {
        "name": fallback_name,
        "report": str(report_path),
        "exists": True,
        "header": header,
        "mtime": datetime.fromtimestamp(stat.st_mtime).strftime("%Y-%m-%d %H:%M:%S"),
        "summary_lines": summary_lines,
    }


