def has_summary_failure(summary: dict[str, Any]) -> bool:
    return execution_failed(summary) or detection_has_issues(summary)


def execution_failed(summary: dict[str, Any]) -> bool:
    if str(summary.get("execution_status") or "").strip() == "失败":
        return True
    return False


def detection_has_issues(summary: dict[str, Any]) -> bool:
    items = list(summary.get("items", []))
    if not items:
        return True

    def normalize_summary_line(value: Any) -> str:
        text = str(value or "").strip()
        return text.rstrip("。.!！?？").strip()

    def item_has_problem(item: dict[str, Any]) -> bool:
        summary_lines = [normalize_summary_line(line) for line in item.get("summary_lines", []) if str(line or "").strip()]
        if not summary_lines:
            return True
        return any(line != "未发现问题" for line in summary_lines)

    return any(item_has_problem(item) for item in items)


def summarize_item_time_text(item: dict[str, Any]) -> str:
    header = str(item.get("header") or "").strip()
    match = re.search(r"(\d{2}/\d{2}/\d{2}-\d{2}:\d{2}:\d{2})", header)
    if match:
        return match.group(1)
    mtime = str(item.get("mtime") or "").strip()
    if mtime:
        try:
            return datetime.strptime(mtime, "%Y-%m-%d %H:%M:%S").strftime("%y/%m/%d-%H:%M:%S")
        except ValueError:
            return mtime
    return ""


